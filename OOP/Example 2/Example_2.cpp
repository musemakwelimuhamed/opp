#include <iostream> // Including the input-output stream library

using namespace std; // Using the standard namespace to avoid writing std:: before cout and cin

int main() // Main function where execution begins
{
    // Declaring and initializing variables
    int age = 20;          // Integer variable to store age
    double price = 19.99;  // Double variable to store price (floating-point number)
    char grade = 'A';      // Character variable to store a grade
    string name = "Alice"; // String variable to store a name

    // Outputting the values of variables
    cout << "Name: " << name << endl;   // Printing name
    cout << "Price: " << price << endl; // Printing price
    cout << "Age: " << age << endl;     // Printing age
    cout << "Grade: " << grade << endl; // Printing grade

    return 0; // Returning 0 to indicate successful program execution
}
